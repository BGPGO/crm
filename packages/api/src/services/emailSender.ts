function personalizeContent(s: string, firstName: string, fullName: string): string {
  return s
    .replace(/\*\|PRIMEIRO_NOME\|\*/g, firstName)
    .replace(/\{\{primeiro_nome\}\}/gi, firstName)
    .replace(/\{\{nome\}\}/gi, fullName);
}

