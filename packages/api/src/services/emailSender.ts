/**
 * Strips the outer document wrapper generated by compileFullHtml() so we get
 * just the inner body content. wrapInBrandTemplate() will re-add the full
 * BGP-branded wrapper, avoiding the "card inside card" double-wrapping issue.
 *
 * Removes: <!DOCTYPE>, <html>, <head>…</head>, <body …>, </body>, </html>,
 * and the outer layout tables (background + content wrapper).
 */
export function stripOuterWrapper(html: string): string {
  let s = html;

  // Remove doctype, html, head, body tags
  s = s.replace(/<!DOCTYPE[^>]*>/gi, '');
  s = s.replace(/<\/?html[^>]*>/gi, '');
  s = s.replace(/<head[\s\S]*?<\/head>/gi, '');
  s = s.replace(/<\/?body[^>]*>/gi, '');

  // Remove the outer centering table (background table → centered table → inner td)
  // Pattern: <table …background-color…><tr><td align="center"…><table …width="6xx"…><tr><td …> CONTENT </td></tr></table></td></tr></table>
  const outerTableRe =
    /^\s*<table[^>]*>\s*<tr>\s*<td[^>]*align=["']center["'][^>]*>\s*<table[^>]*>\s*<tr>\s*<td[^>]*>([\s\S]*)<\/td>\s*<\/tr>\s*<\/table>\s*<\/td>\s*<\/tr>\s*<\/table>\s*$/i;
  const match = s.match(outerTableRe);
  if (match) {
    s = match[1];
  }

  return s.trim();
}

